			<p>Validation therapy is a means through which we accept and support another’s version of ‘reality’. When an individual with dementia begins to lose their short term memory, they often leave the present reality, and revert back to a different time and place. This time and place usually coincides with their past. Your loved one may feel as if he or she is back on the farm, or getting ready for school rather than lying in bed at a Long Term Care Facility. When using Promenade, it is essential not to “correct” your loved one’s reality, but rather, to enter their reality with compassion and understanding. Accepting the reality of the person with dementia not only decreases feelings of anxiety, but also opens up a new world of opportunity for meaningful connection and conversation.</p>
