		<h1>How Does It Help?</h1>

		<p>Promenade provides an alternative approach to communicating with loved ones living with dementia. In view of the proven positive effects of Reminiscence Therapy, Promenade uses photos and conversation-based questions in a way that makes it easier for someone with dementia to reminisce about past life events and pleasant memories. The photos provide the visual cue needed to help trigger the memory, and the questions provide an opportunity for the visiting family member to help expand on that memory and discuss the details surrounding it. Struggling to understand and converse with those we love is a reality that many of us face on a daily basis. With Promenade, both loved ones and their family members can enjoy visits again. </p>
	</div>
</div>

<div class="general">
	<div class="subcolumn">
		
		<h1>What is Reminiscence Therapy?</h1>

		<p>Reminiscence therapy is the heart and soul of Promenade. Reminiscence therapy is a way of thinking back on past activities, events and experiences. It’s a powerful therapeutic approach that often improves mood and brings a person back to a reality they know and more importantly, understand. When living with dementia, less and less of one’s current surroundings make sense. With a dissipating short-term memory, feelings such as confusion, mistrust and anxiety are unfortunately all too common. Through reminiscing, a person with dementia can take pleasure in reviewing positive stories and events from days gone by. Use Promenade’s photos, questions and music to enhance your reminiscing experience.</p>
